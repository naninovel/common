namespace Naninovel.Metadata;

/// <summary>
/// Describes command parameter value type.
/// </summary>
public enum ValueType
{
    /// <summary>
    /// A text string.
    /// </summary>
    String,
    /// <summary>
    /// An integer number.
    /// </summary>
    Integer,
    /// <summary>
    /// A decimal number separated by a dot.
    /// </summary>
    Decimal,
    /// <summary>
    /// A boolean binary value: true/false.
    /// </summary>
    Boolean
}
